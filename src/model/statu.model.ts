export class Statu {
  id: any;
  name: any;
  width: any;
  height: any;
  input: any;
  output: any;
  bgColor: any;

  constructor(
    id: any,
    name: any,
    width: any,
    height: any,
    input: any,
    output: any,
    bgColor: any
  ) {
    this.id = id;
    this.name = name;
    this.width = width;
    this.height = height;
    this.input = input;
    this.output = output;
    this.bgColor = bgColor;
  }
}
